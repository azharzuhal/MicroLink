package Formulir_Pencairan_Dana;

public class FormulirData {
    private String namaDepan;
    private String namaBelakang;
    private String nomorKTP;
    private String alamatLengkap;
    private String bankPilihan;
    private String nomorRekening;
    private String nominalPencairan;
    private String nomorHandphone;
    private String alamatEmail;

    public String getNamaDepan() {
        return namaDepan;
    }

    public void setNamaDepan(String namaDepan) {
        this.namaDepan = namaDepan;
    }

    public String getNamaBelakang() {
        return namaBelakang;
    }

    public void setNamaBelakang(String namaBelakang) {
        this.namaBelakang = namaBelakang;
    }

    public String getNomorKTP() {
        return nomorKTP;
    }

    public void setNomorKTP(String nomorKTP) {
        this.nomorKTP = nomorKTP;
    }

    public String getAlamatLengkap() {
        return alamatLengkap;
    }

    public void setAlamatLengkap(String alamatLengkap) {
        this.alamatLengkap = alamatLengkap;
    }

    public String getBankPilihan() {
        return bankPilihan;
    }

    public void setBankPilihan(String bankPilihan) {
        this.bankPilihan = bankPilihan;
    }

    public String getNomorRekening() {
        return nomorRekening;
    }

    public void setNomorRekening(String nomorRekening) {
        this.nomorRekening = nomorRekening;
    }

    public String getNominalPencairan() {
        return nominalPencairan;
    }

    public void setNominalPencairan(String nominalPencairan) {
        this.nominalPencairan = nominalPencairan;
    }

    public String getNomorHandphone() {
        return nomorHandphone;
    }

    public void setNomorHandphone(String nomorHandphone) {
        this.nomorHandphone = nomorHandphone;
    }

    public String getAlamatEmail() {
        return alamatEmail;
    }

    public void setAlamatEmail(String alamatEmail) {
        this.alamatEmail = alamatEmail;
    }
}
